void Blink()					// P27闪烁3次
{
	P27 = ~P27;						
	delayms(200);				// 第1次闪烁 亮
	P27 = ~P27;
	delayms(200);				// 第1次闪烁 灭
	
	P27 = ~P27;
	delayms(200);				// 第2次闪烁 亮
	P27 = ~P27;
	delayms(200);				// 第2次闪烁 灭
	
	P27 = ~P27;					
	delayms(200);				// 第3次闪烁 亮	
	P27 = ~P27;						
	delayms(200);				// 第3次闪烁 灭
}